package de.hsrm.mi.swt.snackman.entities.Cube;

public class Cube {

    private double width;

    private double height;

    private double depth;

    private double rotationAngleX;

    private double rotationAngleY;

    public double getWidth() {
        return width;
    }

    public void setWidth(double width) {
        this.width = width;
    }

    public double getHeight() {
        return height;
    }

    public void setHeight(double height) {
        this.height = height;
    }

    public double getDepth() {
        return depth;
    }

    public void setDepth(double depth) {
        this.depth = depth;
    }

    public double getRotationAngleX() {
        return rotationAngleX;
    }

    public void setRotationAngleX(double rotationAngleX) {
        this.rotationAngleX = rotationAngleX;
    }

    public double getRotationAngleY() {
        return rotationAngleY;
    }

    public void setRotationAngleY(double rotationAngleY) {
        this.rotationAngleY = rotationAngleY;
    }
}
